#len()
t=(3,4,8,2,99,23,12,56)
l=len(t)
print(l)

#count()
t=(3,4,8,2,99,2,23,2,2,12,56)
l=t.count(2)
print(l)

#shorted()
t=(3,4,8,2,99,2,23,2,2,12,56)
l=sorted(t)
print(l)

#index()
t=('abc','pg','sameer','kiran')
print(t.index('pg'))

#min() and max()
t=(3,4,8,2,99,2,23,2,2,12,56)
m=min(t)
